'''The following program takes input number from user and
processes power 3, 4 and 5 of the respective number and
displays the outputs'''

#taking input from user
num=float(input("Enter a number:"))

#mathematical calculations
power_three=num**3
power_four=num**4
power_five=num**5

#displaying outputs
print("The third power of the given number is:", power_three)
print("The fourth power of the given number is:", power_four)
print("The fifth power of the given number is:", power_five)

