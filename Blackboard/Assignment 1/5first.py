'''The following program takes input of two numbers from the user and 
replaces it in the mathematical equation below and
display the output obtained'''

#asking for user input of first number
a=int(input("Enter the value of 'a':"))

#asking for user input of second number
b=int(input("Enter the value of 'b':"))

#solving the mathematical equation by replacing values
equation= a**2+2*a*b+b**2

#printing the processed output
print("The final value of the equation is:", equation)

