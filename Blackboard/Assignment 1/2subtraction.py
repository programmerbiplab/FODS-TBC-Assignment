'''The following program takes input of two numbers from the user and 
does mathematical operation of subtraction'''

#asking for user input of first number
c=float(input("Enter the first number:"))

#asking for user input of second number
d=float(input("Enter the second number:"))

#doing the mathematical operation of subtraction
subtraction_first=c-d
subtraction_second=d-c

#printing the processed output for both cases of subtraction
print("The subtraction of second number from first number is:", subtraction_first)
print("The subtraction of first number from second number is:", subtraction_second)

