'''The following program displays the square root of
the input number by the user'''

#asking user input
num=float(input("Enter a number:"))

#exponent of the given number raised to 0.5 gives it square root since square root means exponent of 1/2 i.e. 0.5
squareroot=num**0.5

#displaying the square root
print("The square root of the given number is:", squareroot)

