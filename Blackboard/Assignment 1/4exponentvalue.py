'''The following program takes a number input from a user and
exponent value input and displays the output'''

#taking user input
num=float(input("Enter a number:"))
exp=float(input("Enter the exponent value:"))

#mathematical operation
exponent=num**exp

#to display the resulting output of mathematical operation
print(num, "raised to the power of", exp, "is:", exponent)

